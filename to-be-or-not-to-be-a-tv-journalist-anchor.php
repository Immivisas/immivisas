                            <h4 class="post-title"> To be or Not to be a TV Journalist/ Anchor</h4>
                            <p>‘ Never confuse your worth with your work ‘ : This famous quote summarizes the roller coaster ride I’ve had in my career as a television journalist/ anchor. I started my career as an Anchor & Reporter with a leading Hindi news channel in India that gave me instant recognition and valuable life-changing experience. The exposure to the world of journalism gave me confidence and courage to have a voice that would reach millions of viewers. The kick of reporting a story live and interviewing famous people infused an insatiable desire to continue in this profession. However, the flip side of this new found obsession with my profession was odd shifts, no weekly off, and hardly any social life. Despite a lot of hard work, the growth in the salary was minuscule and after a while, this disappointment pushed me to look for greener pastures in other news channels.</p>
                            <br>
							<p>Like most professions, in television too references play a vital role. As luck would have it, I got a job as an anchor and reporter at a very renowned news channel. Initially, it was a lot of fun & a rewarding experience but as the dip in the revenues hit this channel, my professional growth rate slowed down considerably. As time passed it became increasingly clear that a new job hunt was inevitable. Instability in the profession and insufficient raise in the salary were the main factors that instigated me to look for better opportunities. I was jobless for 8 months as some of the job offers I received were either not good enough in terms of salary or not commensurate with my experience. Luckily, I didn’t give in and believed in my worth & caliber.</p>
							<br>
							<p>As luck favors the brave, I got a job offer to work for television shopping channel that was expanding its business & employee strength. This work experience was very new to me and changed my image from a news anchor to a shopping guide. Honestly, my new job title was taken too kindly by my family & friends who thought being a news anchor was more cool & classy. Defying all odds I was determined to acquire new skills and show my prowess at the new workplace. Needless to say, I enjoyed every moment of my time as a tv host on this tv shopping channel and marveled at my own ability to tide through adversity.</p>
							<br>
							<p>To conclude this blog, I’d say that in my experience television career is all about reinventing yourself. Be prepared for big ups and downs in your career and don’t be afraid to try new opportunities as every day at a television channel is a new day that can bring fame, recognition & monetary benefits. Believe in your worth & never confuse it with the work you are assigned.</p>
							<br>
							<p>By – Namrata Sharma</p>
                           
