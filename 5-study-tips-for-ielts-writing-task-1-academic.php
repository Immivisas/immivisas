                            <h4 class="post-title">5 Study tips for IELTS Writing Task 1 (Academic)
</h4>
                            <p>Success in the IELTS writing section in not tough if you study smart and use the right techniques. Here are some tips to help you ace this section.</p>
                            <br>
							<p>1. Understand the Format</p>
							<br>
							<p>The IELTS writing task 1 is a data interpretation/explanation task. You will be given 20 minutes to write at least 150 words.  This task requires you to present some given data in words. The data can be in the form of a graph, diagram, chart, table or a map. The key is to not add ANY information and report only what the data depicts.</p>
							<br>
							<p>2. Vocabulary</p>
							<br>
							<p>The IELTS exam does not expect you to use difficult and complicated words. However, it is extremely important that you familiarize yourself with specific vocabulary for data interpretation. The reason is that some questions may require you to use specific terminology for which there is no alternative. For example you may want to report increase in car ownership over 2 decades. Even though ‘increase’ is an appropriate word and can be used as many times as it is required, you may want to diversify with it’s synonyms or even different forms (in terms of active/passive, tense etc). This will help you get closer to a band 9.</p>
							<br>
							<p>3. Prepare a Template of your answers</p>
							<br>
							<p>If you do not feel confident with your writing try to prepare a template for every type of question within each task.  This may seem like a lot of work but it will reduce your thinking time during the exam. Read the example below.</p>
							<br>
							<p>The following data shows consumption of drinks in USA and England in 2010.</p>
							<br>
							<img src="assets/img/bar-chart-1.jpg">
							<br><br>
							<p>
                            Now here is a template to write the introduction. Fill in the blanks carefully, add details and omit what isn’t required.<br>
                           The Type of Chart illustrates/depicts/shows the number/percentage/amount of ______ (further classified by ______ ) in place and/or time period. Units are measures in ________ . *</p><br>
						   <p>The Bar Chart illustrates the amount of consumption of 4 drinks (Orange juice, beer, milk, water) in USA and England in 2010. Units are measured in million litres.</p><br>
						   <p>*This may not fit every question category but can be helpful to begin with.</p><br>
                            <p>4. Grammar</p>
							<br>
                            <p>Sentence Construction is very crucial and helps the examiners learn whether your answer is memorized. Remember, IELTS gives no marks for memorized sentences. Even while writing the introduction it is better to paraphrase (write in your own words) the question and not repeat it.  Sentence constructs need to be correct and varied throughout the report. Correct verb forms, tenses hold equal importance in IELTS.</p>
							<br>
                            <p>5. Task Completion and Organization</p>
							<br>
							<p>In most cases the question will ask you to summarize, report key features and make comparisons. Firstly, you should practice how to read a graph or a diagram. Begin with reading the topic, x-axis and the y-axis. Notice any upward and downward trends, highest and lowest data points and anomalies. Comparisons can be generic and specific. Most students do not have a hard time finding points worth comparing. The key is to write it correctly.</p>
							<br><p>Here is an example of the most common mistake: Faulty Comparison.</p><br>
							<br><p>Incorrect – The consumption of milk in England is more than USA.</p><br>
							<br><p>The above sentence compares ‘consumption of milk’ to ‘USA’.</p><br>
							<br><p>Correct – The consumption of milk in England is more than consumption of milk in USA.</p><br>
							<br><p>Once you identify the main features you will be required to write it in an organized way, in preferably 3-4 paragraphs which include the Introduction, Overview, Details and Summary/Conclusion.</p><br>
