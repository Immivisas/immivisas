                            <h4 class="post-title">5 Tips to prepare for College Applications
</h4>
                            <p>It is that time of the year again! Over the past few weeks top Universities all around the world have started opening their applications for 2019. For most of the Universities applications for Round 1 will open next month! The big question is, are you ready ? If not, here are 5 tips to help you prepare for the big battle.</p>

<p><strong>1. Be familiar with your Deadlines!!</strong></p>

<p>Application deadlines for international students are usually set sooner than for domestic applicants. So if you are planning on studying abroad, the sooner you apply the better it is. Universities do open applications for Round 2 or Round 3 or until the seats are occupied. However, if you plan to apply to the top Universities you may have a better chance of getting selected in the first Round.</p>
<p><strong>2. Prepare your Essays</strong></p>
<p>Some Universities release their essay questions a few weeks before the online applications open.&nbsp; This is great as you will get some time to organize your thoughts in the back of your head. Even if the essay questions have not been released, try to think of answers to questions like, ‘Where do you see yourself in the next 5 years?’, ‘Why did you chose this particular University’, ‘What are your goals in life?’ and so on. If you have no idea, get some professional help to guide you. However, you should know that a professional can only guide you and SHOULD NOT write the essay on your behalf. No one knows about your life, dreams and aspirations better than you.</p>
<p><strong>3. Keep you scores ready!</strong></p>
<p>If you have already sat for your first attempt of GMAT or SAT , great! If that is not the case, no worries! Universities are aware of the process and you can let them know when your result is expected. They encourage students to aim for better scores and will only consider the higher score whether it is SAT, IELTS, TOEFL or GMAT .</p>
<p><strong>4. Shortlist your Recommenders</strong></p>
<p>Prepare a list of recommenders you will need for your applications. Make sure that these recommenders are aware of your personal and professional traits. Some Universities specify whether they want an academic or a professional reference. Hence it is better to prepare a list of all potential recommenders.</p>
<p><strong>5. Put your Transcripts in order</strong></p>
<p>You may also want to inform your school or University that you will be requiring sealed transcripts to send, once you get accepted. Until then keep scanned pdf copies of all the unofficial transcripts ready for submission.</p>
<p>It is never too early to get started!</p>
