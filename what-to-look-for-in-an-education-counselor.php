                            <h4 class="post-title">What to look for in an Education Counselor?
</h4>
                          <p>If you are a student who is looking for an education consultant to help you with your college applications, behold! There are thousands of options to choose from and it can get a little overwhelming. To make your decision easier, we have put together a list of pointers to look for in your counselor.</p><br>
<p><strong>1. Numbers lie.</strong></p><br>
<p>Do not fall for the bulk of students your counselor sends to college every year. What should matter is how their previous students are performing AFTER they completed the course ? In how much time were they able to find a job? What kind of salaries do they have now? These are the questions you need to ask. So make sure you do your research.</p><br>
<p><strong>2. Beware of free consultations!</strong></p><br>
<p>It is said that if you are using a product/service for free, then YOU are the product ! Nobody gives anybody anything for free. Understand they WILL have an agenda behind offering you something for no charge! Most counselor’s in the market have affiliations with universities. So they will push you for a university and a course where their commission lies. It is obvious to say that they will not care about what is beneficial for you.</p><br>
<p><strong>3. Knowledge of immigration</strong></p><br>
<p>If you are looking to study abroad, there are chances you may want to work there after. Make sure your counselor gives you clear answers to questions like, Will I get a work permit after I complete the course ? For how many years will I be able to work ? Will I qualify for PR ? How is the job market like for the course you will pursue?</p><br>
<p><strong>4. Transparent Admission Process</strong></p><br>
<p>Since most applications are made online, make sure that your counselor shares the login details of every application. One should also understand that a counselor’s service charge should NOT include the application fees. If it does, ask for a break up of all costs that you will incur. Also once they are done with filling your application for you, prefer to make the application fee payment yourself.</p><br>
<p>If you have no idea what we are talking about, here is the difference between a counselor’s charge and application fee. A Counselor’s Fee is what he charges for the service he provides. This includes guidance, counseling, short listing Universities and courses for you, aptitude tests etc. An Application Fees is charged by the University. You can think of it as cost of filling up the University form. This is directly paid to the University through their online applications on their website or a common portal.</p><br>
<p><strong>5. A Counselor who talks about job prospects</strong></p><br>
<p>Getting into a university or a course should not be your goal. The goal should be to find the work you want to do after you are done with the course. Thus knowledge about the job market is as important as the Universities and the courses they offer! A degree will be as good as a piece of paper if it doesn’t help you find work.</p><br>
<p>All the best !!</p>
                           
							
