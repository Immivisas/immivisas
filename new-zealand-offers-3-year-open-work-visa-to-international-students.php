                            <h4 class="post-title">New Zealand offers 3 Year Open Work Visa to International Students
</h4>
                            <p>With effect from 26 November 2018, students successfully completing bachelors, masters or doctoral in New Zealand will be eligible for a 3 year open work visa. An ‘Open’ Work Visa holds a lot of importance as students will not require companies/employers to sponsor them a work visa. They can work in any job, for any employer and anywhere in New Zealand post study.</p>
                            <br>
							<p>Moreover international students who pursue</p>
							<br>
							<p>1.   Level 7 or Level 8 (Graduate Certificate/ Graduate Diploma / Bachelors Degree / postgraduate certificate/ postgraduate diploma / bachelors honors degree) provided the field of study is mentioned in the New Zealand Long Term skills shortage List    OR</p>
							<br>
							<p>2.   Masters or Doctoral studies will be entitled to,</p>
							<br>
							<p>a) Open Work Visa for Spouse AND</p>
							<br>
							<p>b) Free Domestic Schooling for dependent children.</p>
							<br>
							<p>Successful completion of the following levels of education will make you eligible for open post-study work visas of one, two or three years.</p>
							<br>
							<img src="assets/img/NZ-table.jpg">
							<br><br>
							<p>
                            ** Following at least 60 weeks of study<br>
                            * Following at least 30 weeks of study</p><br>
                            <p>These changes will make it a lot easier for many students and workers to obtain permanent citizenship of New Zealand. Though New Zealand has taken these steps to invite more highly-skilled labor to their economy, these policies will benefit thousands of international students and workers.</p>
							<br>
                            <p>For more information visit www.immigration.govt.nz</p>
							<br>
