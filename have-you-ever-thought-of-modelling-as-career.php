                            <h4 class="post-title">Have you ever thought of Modelling as career ?
</h4>
                            <p>Modelling is a highly competitive industry with increasing number of model every year. But with the right network and knowledge it can be turned into a full time profession. Here is everything you need to know.</p>
                            <br>
							<p>WHAT PREPARATION DO I NEED TO BECOME A MODEL ?</p>
							<br>
							<p>If you inherited good looks from your parents, it is not enough. Modelling as a profession requires you to be extremely fit so it goes without saying that you have to exercise and be diet conscious. Mostly Models are expected to have a lean body structure and they also need to take good care of their skin and hair.However, with ongoing debate on positive body image the industry for models has become a little lenient with it’s standards.</p>
							<br>
							<p>WHAT IS THE RIGHT AGE TO FIND WORK ?
</p>
							<br>
							<p>If you are in the age bracket of 20 to 25 years finding work is relatively easy. The age category of 25 to 35 years is the category of adult models and work is still available through various mediums of Modelling. Once you have crossed the age of 35 years, you may not work as a runway model but you can get work through television commercials and Print Modelling. It’s a well known fact that fashion Industry is more lucrative for female models. On an average, the shelf life of a male ramp model is only two to four years, which is shorter than a female model’s shelf life.

</p>
							<br>
							<p>HOW MUCH DO MODELS GET PAID ?</p>
							<br>
							<p>Models are required at various events like auto-expo, product launches etc.
At such events models are paid approximately 5000-15000 rupees per day (depending upon the brand for which they are selected). For a print shoot or a photo shoot a fresher can get Rs. 5000-10000.The shoot generally lasts for 4–5 hours. For the ramp, an established model with 8-10 years of experience charges between 25,000 and 30,000 per show. It does take a while though to earn high amounts in modelling, the more popular you are as a model the more money you’ll make.<br>
Models usually earn money through their agencies, advertising or through events. Models who don’t sign up with an agency, operate through model coordinators. A model coordinator takes a commission between 20 to 30 percent from the model’s fees/Income. Magazines, Fashion Shows, and beauty products are industries that hire models through agencies and pay very well.</p>
							<br>
							<p>WHAT TYPE OF WORK DO MODELS DO ?</p>
							<br>
							<ol>
<li><b>Fashion models promote clothing and apparel.<br>
</b></li>
<li><b>High fashion models work with the famous fashion houses or designers.<br>
</b></li>
<li><b>Editorial models only work for certain publications.<br>
</b></li>
<li><b>Runway models work at fashion shows.<br>
</b></li>
<li><b>Showroom models display clothing at fashion parties or boutiques.<br>
</b></li>
<li><b>Commercial print models are photographed for magazines, newspapers, billboards and other print ad materials</b></li>
<li><b>&nbsp;</b><b>Television Models are auditioned by Production houses and finalized&nbsp;by the Client.</b></li>
</ol><br>
