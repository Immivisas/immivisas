                            <h4 class="post-title">Difference between CBSE, ICSE, IGCSE, IB and State Board
</h4>
                           <p>Difference between CBSE, ICSE, IGCSE, IB and State Board<br />
With the Beginning of admissions’ season across cities in India, parents face a dilemma as to which board would fit best for their young ones.</p>
<p>India Schools offer both Indian and International Syllabi namely:</p>
<p>1. CBSE: Central Board of Secondary Education</p>
<p>2. CISCE: Council for the Indian School Certificate Examinations (CISCE)</p>
<p>3. IB: International Baccalaureate</p>
<p>4. IGCSE: International General Certificate of Secondary Education (IGCSE) Cambridge University</p>
<p>5. State Board: State Government Recognized Board</p>
<p>Let’s consider each one of them….</p>
<p>A. CBSE Central Board of Secondary Education</p>
<p>A Board that follows universal pattern is the first choice for parents with transferable jobs.</p>
<p>CBSE affiliates all Kendriya Vidyalayas, all Jawahar Navodaya Vidyalayas, private schools, and most of the schools approved by the central government of India</p>
<p>Examination Pattern: All India Senior School Certificate Examination (AISSCE) for Class 10 and 12</p>
<p>Facts</p>
<p>Total Number of Schools Affiliated with CBSE: 14808<br />
Areas of Operation: 25 Countries including India, Malaysia, Dubai, etc<br />
Major entrance exams syllabus based on CBSE viz. IIT, AIPMT, etc<br />
Pros</p>
<p>1) The diversity factor in schools with CBSE board is high as more students from different backgrounds are likely to end up in the same class.</p>
<p>2) Quality of Content (No Errors/ No Printing Mistakes) in Textbooks’ is good.</p>
<p>3) Continuous And Comprehensive Evaluation (CCE), a new system of education implemented by CBSE for students of sixth to tenth grades. It removes the marking system and introduces a grading system based on a series of curricular and extracurricular activities.</p>
<p>4) Easy to find tutors, books and activities for all classes.</p>
<p>5) Focus on Science and Mathematics as well as application-based subjects.</p>
<p>6) Recognition by all colleges in India.</p>
<p>7) Centralized system for transferring across all CBSE schools.</p>
<p>8) Numerous talent search examinations and scholarship exams such as SSTSE, NSEB, NSEC, etc held at local and national levels base their content on CBSE syllabus just because it is more prevalent.</p>
<p>Cons</p>
<p>1) Although Major Entrance Exams syllabus is based on CBSE, the fact that the seats for those courses are open for all students in India. And colleges that are coming under state universities have more reserved seats for students from state boards. So students from CBSE board who wouldn’t crack entrance exams face problems in getting admission in-state university colleges as they have very less number of seats for CBSE Board quota.</p>
<p>2) Fees can be on the high side for some schools.</p>
<p>3) There aren’t enough options to explore in the field of Arts and Literature</p>
<p>B. CISCE Council for the Indian School Certificate Examinations (CISCE)</p>
<p>Examination Pattern: This body conducts 3 examinations, namely</p>
<p>• ICSE (Indian certificate secondary education) exam for class 10</p>
<p>• ISC (Indian school certificate) exam for class 12</p>
<p>• CVE (Certificate for vocational education) exam for class 12</p>
<p>Facts</p>
<p>• Total Number of Schools Affiliated with CISCE: Approx. 1000</p>
<p>• Areas of Operation: India, UAE, Singapore, Indonesia</p>
<p>• Subjects Offered ICSE in 2013-64 with 23 Indian languages and 12 are foreign languages.</p>
<p>• Subjects Offered ISC in 2013- 50 with 23 Indian languages and 12 are foreign languages.</p>
<p>Pros</p>
<p>1. The course curriculum is wide, covering a range of topics to explore the overall growth of students.</p>
<p>2. CISCE prefers to give more practical Knowledge and focus more on analytical skills.</p>
<p>3. Subject selection options available to students to follow their liking for a particular subject.</p>
<p>4. The CISCE board has an equal focus on languages, science, and art and encourages students to choose across diverse topics/subjects for their Class-12 exam.</p>
<p>Cons</p>
<p>1. Students may find the syllabus a bit cumbersome.</p>
<p>2. Students would find it hard to shift to other boards or Pre-Science Colleges (11-12 Standards)</p>
<p>3. Higher Fees</p>
<p>4. Although Major Entrance Exams syllabus is based on CISCE, the fact that the seats for those courses are open for all students in India. And colleges that are coming under state universities have more reserved seats for students from state boards. So students from the CISCE board who wouldn’t crack entrance exams face problems in getting admission in-state university colleges as they have very few seats for CISCE Board.</p>
<p>C. IB International Baccalaureate (IB), formerly the International Baccalaureate Organization (IBO), is an international educational foundation headquartered in Geneva, Switzerland and founded in 1968</p>
<p>Examination Pattern: It offers 3 educational programs, namely</p>
<p>• PYP or Primary year program for KG to Class-5</p>
<p>• MYP or Middle year program for Class-6 to Class-10</p>
<p>• DP or Diploma program for Class-11 and Class-12</p>
<p>Facts</p>
<p>• Number of Schools: Approx 3500 worldwide &amp; 130 in India.</p>
<p>• Areas of Operations: 144 Countries including India, USA, UK, Australia, etc.</p>
<p>Pros</p>
<p>1. International Board recognized by most of the Universities of the world.</p>
<p>2. Students can apply to Foreign Universities wherein CBSE and ICSE board is not recognized.</p>
<p>3. Recognized by UNESCO, Council of Europe, Organisation Internationale de la Francophonie (OIF)</p>
<p>4. Curriculum-based on application and experimentation.</p>
<p>5. Better Infrastructure of Schools.</p>
<p>6. The best option if parents are NRI, or are planning to move abroad.</p>
<p>Cons</p>
<p>1. The fee is on the higher side.</p>
<p>2. Found only in metropolitan cities and Tier-1 cities of India</p>
<p>3. Difficult for students to shift boards if the need arises.</p>
<p>4. Tuitions, Books not easily available.</p>
<p>D. IGCSE International General Certificate of Secondary Education (IGCSE) is an academically rigorous, internationally used, specialized, English language curriculum that is offered to students to prepare them for International Baccalaureate and CIE A-level.</p>
<p>Examination Pattern</p>
<p>• Cambridge Primary, typically for learners aged 5 to 11 years</p>
<p>• Cambridge Secondary 1</p>
<p>• Cambridge Secondary 2 is typically for learners aged 14 to 16 years. It offers learners two routes: Cambridge IGCSE and Cambridge O Level.</p>
<p>• Cambridge Advanced is typically for learners aged 16 to 19 years who need advanced study to prepare for university and higher education. It offers learners two routes: Cambridge International AS and A Level, and Cambridge Pre-U.</p>
<p>Facts</p>
<p>• Number of Schools: Approx 200 in India</p>
<p>• Areas of Operations: 120 Countries including India, USA, UK.</p>
<p>Pros</p>
<p>1. International Board recognized by most of the Universities of the world.</p>
<p>2. Students can apply to Foreign Universities wherein CBSE and ICSE board is not recognized.</p>
<p>3. More advanced curriculum and a variety of subjects to choose from.</p>
<p>4. More emphasis on English and Technical Subjects</p>
<p>5. The best option if parents are NRI, or are planning to move abroad.</p>
<p>Cons</p>
<p>1. The fee is on the higher side.</p>
<p>2. Found only in metropolitan cities and Tier-1 cities of India</p>
<p>3. Difficult for students to shift boards if the need arises.</p>
<p>4. Tuitions, Books not easily available.</p>
<p>E. State Board</p>
<p>Every State Government imparts Primary, Secondary and Higher Secondary Education in its respective state.</p>
<p>Examination Pattern</p>
<p>• SSC: Secondary School Certificate, 10 Board Exams</p>
<p>• HSC: Higher School Certificate, 12 Board Exams</p>
<p>Facts</p>
<p>• Areas of Operations: Every State and Union Territory of India</p>
<p>Pros</p>
<p>1. Favorable for parents without Transferable Jobs.</p>
<p>2. Easily available Textbook, Teachers.</p>
<p>3. Moderately loaded curriculum giving options for Students to explore more extracurricular activities like Dancing and Sports.</p>
<p>4. Lower Fees.</p>
<p>5. More reserved seats within the state-recognized colleges</p>
<p>Cons</p>
<p>1. It depends wholly on the State government’s competence to maintain the quality of education.</p>
<p>2. Quality of Teaching varies according to school.</p>
<p>3. Might Lack basic facilities.</p>
<p>4. Quality of Content (No Errors/ No Printing Mistakes) may be questionable.</p>
